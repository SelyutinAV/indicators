        echo -e "${YELLOW}–ü–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–µ –Ω–∞ –≤–µ—Ç–∫—É $BRANCH...${NC}"
        git checkout "$BRANCH" 2>/dev/null || git checkout -b "$BRANCH" "origin/$BRANCH"
    fi
    
    # –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∫–æ–¥–∞
    git pull origin "$BRANCH"
    echo -e "${GREEN}‚úÖ –ö–æ–¥ –æ–±–Ω–æ–≤–ª–µ–Ω${NC}"
