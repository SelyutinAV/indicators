        echo "   –ü–µ—Ä–µ–º–µ—â–µ–Ω–∏–µ –≤ —Ä–µ–∑–µ—Ä–≤–Ω—É—é –∫–æ–ø–∏—é..."
        BACKUP_NAME="${PROJECT_DIR}.backup.$(date +%Y%m%d_%H%M%S)"
        mv "$PROJECT_DIR" "$BACKUP_NAME"
        echo -e "${GREEN}‚úÖ –†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Å–æ–∑–¥–∞–Ω–∞: $BACKUP_NAME${NC}"
    fi
    
    # –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ
    cd "$PARENT_DIR"
    PROJECT_NAME=$(basename "$PROJECT_DIR")
    echo -e "${YELLOW}–ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ –∏–∑ $GIT_REPO –≤ $PROJECT_NAME...${NC}"
    git clone -b "$BRANCH" "$GIT_REPO" "$PROJECT_NAME"
    cd "$PROJECT_DIR"
    echo -e "${GREEN}‚úÖ –†–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π —Å–∫–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω${NC}"
